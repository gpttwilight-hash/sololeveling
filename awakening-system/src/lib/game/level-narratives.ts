const DEFAULT_NARRATIVES = [
  "Система фиксирует рост. Продолжай.",
  "Каждый выполненный квест — голос за того, кем ты становишься.",
  "Паттерн укрепляется. Привычка формируется.",
  "Система подтверждает прогресс охотника.",
  "Атрибуты растут. Личность меняется.",
];

export function getLevelNarrative(level: number): string {
  if (LEVEL_NARRATIVES[level]) return LEVEL_NARRATIVES[level];
  // Find nearest milestone below
  const milestones = Object.keys(LEVEL_NARRATIVES).map(Number).sort((a, b) => b - a);
  for (const m of milestones) {
    if (level > m) return DEFAULT_NARRATIVES[level % DEFAULT_NARRATIVES.length];
  }
  return DEFAULT_NARRATIVES[level % DEFAULT_NARRATIVES.length];
}

export function getRankNarrative(rank: string): string {
  return RANK_NARRATIVES[rank] ?? `Система фиксирует новый ранг: ${rank}.`;
}
